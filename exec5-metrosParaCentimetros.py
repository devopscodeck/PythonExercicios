# Escreva um programa que converta métros para centímetros :

metro = float(input("Digite o valor que será convertido em metros para centímetros: "))
conversao_metro_para_centimetro = metro * 100
print("Em centímetros você teráo equivalente a: ", conversao_metro_para_centimetro, "centímetros")